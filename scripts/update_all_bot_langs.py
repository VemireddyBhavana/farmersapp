# Update each block
new_content = content
for lang in languages:
    if lang not in translations_data: continue
    
    pattern = rf'^\s+{lang}:\s*\{{'
    match = re.search(pattern, new_content, re.MULTILINE)
    if match:
        start_idx = match.start()
        bracket_count = 0
        end_idx = -1
        for i in range(match.end() - 1, len(new_content)):
            if new_content[i] == '{':
                bracket_count += 1
            elif new_content[i] == '}':
                bracket_count -= 1
                if bracket_count == 0:
                    end_idx = i + 1
                    break
        
        if end_idx != -1:
            block = new_content[start_idx:end_idx]
            for key, val in translations_data[lang].items():
                block = safe_replace_key(block, key, val)
            new_content = new_content[:start_idx] + block + new_content[end_idx:]

with open(file_path, 'w', encoding='utf-8') as f:
    f.write(new_content)

