def safe_replace_key(block, key, value):
    pattern = rf'{key}:\s*".*?"'
    if re.search(pattern, block):
        return re.sub(pattern, f'{key}: "{value}"', block)
    else:
        last_brace = block.rfind('}')
        if last_brace != -1:
            insertion = f'    {key}: "{value}",\n'
            return block[:last_brace] + insertion + block[last_brace:]
        return block

