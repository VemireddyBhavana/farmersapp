def update_lang_block(content, lang, translations):
    start_pattern = rf'{lang}:\s*\{{'
    match = re.search(start_pattern, content)
    if not match:
        return content
    
    start_idx = match.start()
    bracket_count = 0
    end_idx = -1
    for i in range(start_idx + len(match.group(0)) - 1, len(content)):
        if content[i] == '{':
            bracket_count += 1
        elif content[i] == '}':
            bracket_count -= 1
            if bracket_count == -1:
                end_idx = i
                break
    
    if end_idx == -1:
        return content
        
    block = content[start_idx:end_idx]
    
    # We want to replace or add keys
    for key, val in translations.items():
        if f' {key}:' in block or f'\n    {key}:' in block:
            # Replace
            block = re.sub(rf'({key}:\s*").*?(")', rf'\1{val}\2', block)
        else:
            # Append to the end of the block
            block = block.rstrip()
            if block.endswith(','):
                block += f'\n    {key}: "{val}",'
            else:
                block += f',\n    {key}: "{val}",'
                
    return content[:start_idx] + block + content[end_idx:]

